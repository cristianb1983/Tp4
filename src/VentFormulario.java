    private void jbInscribirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbInscribirActionPerformed
        
        Alumno alumno = (Alumno) jcbAlumno.getSelectedItem();
        Materia materia = (Materia) jcbMateria.getSelectedItem();
        
        if (alumno != null && materia != null) {
            boolean inscripto = alumno.agregarMateria(materia);
            if (inscripto) {
                JOptionPane.showMessageDialog(this, "Inscripci칩n exitosa: " + alumno.getApellido() + " se inscribi칩 en materia " + materia.getNombre());
            } else {
                JOptionPane.showMessageDialog(this, "Error: El alumno " + alumno.getApellido() + " ya est치 inscripto en materia " + materia.getNombre());
            }
        } else {
            JOptionPane.showMessageDialog(this, "Error!!! Debe seleccionar materia y alumno");
        }
        
        jcbMateria.setSelectedIndex(0);
        jcbAlumno.setSelectedIndex(0);
        
    }//GEN-LAST:event_jbInscribirActionPerformed

